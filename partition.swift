// print partition array
func printPartition(_ p:[Int], _ r:Int) {
    for i in 0..<r {
        print(" ", p[i], terminator:"")
    }
    print("")
}

// This function finds all the partitions for the given 2 integers (n and r)
func findPartitions(_ n:Int, _ r:Int) {
    var partition = Array(repeating: 0, count: n)    // An array to store a partition (full of 0s in order to store numbers)
    var index:Int = 0                                // Index of last element in a partition
    partition[index] = n                             // Initialize first partition as number given itself
    
    // repeat this process for every number through n to -1, when its -1, exit the loop !
    while true {
        // this algorithm finds all possible partitions
        // however the ones that will be printed are the ones that uses r integers
        // since the partion array is not a 2D array, this loop prints out each new array from the same initial array
        if index + 1 == r {
            printPartition(partition, r)
        }
        
        var remainderValue:Int = 0                                      // remainder value used to store the sum of the partitions for each array (like a counter)
        
        // repeat this loop as long as the index is at least 0 and the partition array is near the end
        while index >= 0 && partition[index] == 1 {
            remainderValue = remainderValue + partition[index]
            index -= 1
        }
        
        // when the index is -1, exit the loop !
        if index < 0 {
            return
        }
        
        partition[index] -= 1       // decreases the number in the partition
        remainderValue += 1         // increase the value
        
        // while the remainder counter is greater than the number in the partion
        while remainderValue > partition[index] {
            partition[index + 1] = partition[index]                 // assign the value from later on to be equal to the current value
            remainderValue = remainderValue - partition[index]      // change the remainder value to be smaller by decreasing it to the current number value in the partion array
            index += 1                                              // increase the index value
        }
        // change values once more
        partition[index + 1] = remainderValue
        index += 1
    }
}

// Ask the user for an integer greater than 0
func getInteger() -> Int {
    var value:Int = 0
    while true {
        // get value from user. Using optional input since readLine returns an optional string.
        guard let inputLine = readLine() else {
            fatalError("Unexpected end-of-file condition")
        }
        
        // attempt to convert the input string to an Int
        if let unwrappedInt = Int(inputLine) {
            // Only use the value inputted by the user if it's between 0 and 20
            if unwrappedInt > 0 {
                value = unwrappedInt
                break
            }
            else {
                print("`\(inputLine)` is not greater than 0. Please enter a positive integer")
            }
        }
            // the input doesn't convert into an int
        else {
            print("`\(inputLine)` is not an integer. Please enter a positive integer")
        }
    }
    return value
}

